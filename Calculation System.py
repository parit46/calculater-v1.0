def temperatureconverter_calculate():
    try:
        value = float(entry7.get()) 
        from_unit = from_var.get()
        to_unit = to_var.get()

        # Convert input to Celsius first
        if from_unit == "°C":
            c = value
        elif from_unit == "K":
            c = value - 273.15
        elif from_unit == "°F":
            c = (value - 32) * 5/9
        elif from_unit == "°Ra":
            c = (value - 491.67) * 5/9
        elif from_unit == "°Re":
            c = value * 5/4
        else:
            result_var.set("Unknown unit")
            return

        # Convert Celsius to target unit
        if to_unit == "°C":
            result = c
        elif to_unit == "K":
            result = c + 273.15
        elif to_unit == "°F":
            result = c * 9/5 + 32
        elif to_unit == "°Ra":
            result = (c + 273.15) * 9/5
        elif to_unit == "°Re":
            result = c * 4/5
        else:
            result_var.set("Unknown unit")
            return

        result_var.set(f"{result:.2f} {to_unit}")
    except:
        result_var.set("Error")

def fibonaccisequence_calculater():
    try:
        n = int(entry9.get())
        sequence = []

        a, b = 0, 1
        for _ in range(n):
            sequence.append(a)
            a, b = b, a + b

        result9.config(text="ผลลัพธ์: " + str(sequence))

    except ValueError:
        result9.config(text="กรุณากรอกเลขจำนวนเต็ม")

